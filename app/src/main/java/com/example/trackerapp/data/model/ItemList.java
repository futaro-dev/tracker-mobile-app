package com.example.trackerapp.data.model;

public class ItemList {
    public String id;
    public String name;

    public ItemList() {}

    public ItemList(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
